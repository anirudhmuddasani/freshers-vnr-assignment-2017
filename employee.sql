---- creating an database for the employee in the restaurant


USE  DATABASE foodielove;

CREATE TABLE employee(
	employee id int UNIQUE ,
	name        varchar(15),
	department id  id,
	date of joining date ,
	salary	     money,
	designation varchar,
	
	primary key (department .id)

);
CREATE TABLE department (
	deptname varchar (30),
	dept id  money,
	primary key (dept.id)

);

